import axios from "axios";
import { accountActivationPath } from "../urls";

export const fetchAccountActivation = (activationToken) => {
  return axios.get(accountActivationPath(activationToken))
    .then(res => {
      console.log('accountActivation#edit', res);
      return res.data;
    })
    .catch(error => {
      console.log('accountActivation#edit', error);
    });
}
